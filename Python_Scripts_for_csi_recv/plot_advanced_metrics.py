def plot_class_distribution(
    cfg: dict, data_dir: Path,
    window_size: int, step: int,
    save: bool, save_dir: Path, no_show: bool,
):
    """Recordings and estimated windows per class (train vs test split)."""
    if not cfg:
        print("[SKIP] Class distribution: experiment_config.json not available.")
        return

    train_files = cfg.get("train_files", {})
    test_files  = cfg.get("test_files",  {})
    classes     = cfg.get("classes", sorted(set(list(train_files) + list(test_files))))

    # Count recordings
    rec_train = [len(train_files.get(c, [])) for c in classes]
    rec_test  = [len(test_files.get(c,  [])) for c in classes]

    # Count estimated windows (scan frame counts)
    print("[INFO] Counting frames for window estimation…")
    def _win_count_for_files(files_list):
        total = 0
        for rel in files_list:
            n = _count_frames(data_dir / rel)
            total += _estimate_windows(n, window_size, step)
        return total

    win_train = [_win_count_for_files(train_files.get(c, [])) for c in classes]
    win_test  = [_win_count_for_files(test_files.get(c,  [])) for c in classes]

    _apply_style()
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=config.PLOT_ADVANCED_METRICS_SIZE)
    fig.patch.set_facecolor(STYLE["bg"])

    x     = np.arange(len(classes))
    width = 0.35

    # --- Recordings ---
    bars_tr = ax1.bar(x - width / 2, rec_train, width,
                      label="Train", color=STYLE["a1"], edgecolor="white")
    bars_te = ax1.bar(x + width / 2, rec_test,  width,
                      label="Test",  color=STYLE["a3"], edgecolor="white")
    ax1.set_xticks(x)
    ax1.set_xticklabels([c.capitalize() for c in classes],
                        fontsize=11, fontweight="bold")
    ax1.set_ylabel("Number of Recordings", fontsize=11, fontweight="bold")
    ax1.set_title("Recordings per Class\n(Train / Test Split)",
                  fontsize=12, fontweight="bold")
    ax1.legend(fontsize=10, framealpha=0.9)
    ax1.bar_label(bars_tr, fmt="%d", padding=3, fontweight="bold", fontsize=10)
    ax1.bar_label(bars_te, fmt="%d", padding=3, fontweight="bold", fontsize=10)

    # --- Estimated windows ---
    bars_wtr = ax2.bar(x - width / 2, win_train, width,
                       label="Train", color=STYLE["a1"], edgecolor="white")
    bars_wte = ax2.bar(x + width / 2, win_test,  width,
                       label="Test",  color=STYLE["a3"], edgecolor="white")
    ax2.set_xticks(x)
    ax2.set_xticklabels([c.capitalize() for c in classes],
                        fontsize=11, fontweight="bold")
    ax2.set_ylabel("Estimated Windows", fontsize=11, fontweight="bold")
    ax2.set_title(
        f"Windows per Class\n(window={window_size} frames, step={step} frames)",
        fontsize=12, fontweight="bold")
    ax2.legend(fontsize=10, framealpha=0.9)
    ax2.bar_label(bars_wtr, fmt="%d", padding=3, fontweight="bold", fontsize=10)
    ax2.bar_label(bars_wte, fmt="%d", padding=3, fontweight="bold", fontsize=10)

    # Totals annotation
    total_rec = sum(rec_train) + sum(rec_test)
    total_win = sum(win_train) + sum(win_test)
    fig.suptitle(
        f"Dataset Distribution — {total_rec} recordings total  |  "
        f"~{total_win} windows total  |  {len(classes)} classes",
        fontsize=13, fontweight="bold",
    )
    fig.tight_layout(rect=[0, 0, 1, 0.93])

    if save:
        _save_fig(fig, save_dir, "adv_01_class_distribution")
    if not no_show:
        show_all(figs=[fig])
    plt.close(fig)


