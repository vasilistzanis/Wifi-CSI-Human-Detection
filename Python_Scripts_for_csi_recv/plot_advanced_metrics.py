# -----------------------------------------------------------------------

def plot_train_cv_test_gap(
    metrics: dict,
    save: bool, save_dir: Path, no_show: bool,
):
    """Grouped bars: Train accuracy, CV mean, Test accuracy per model."""
    model_names = list(metrics.keys())
    n_models    = len(model_names)

    has_train = any("train_accuracy" in v for v in metrics.values())

    _apply_style()
    fig, ax = plt.subplots(figsize=(max(9, n_models * 1.6), 6))
    fig.patch.set_facecolor(STYLE["bg"])

    x     = np.arange(n_models)
    n_grp = 3 if has_train else 2
    width = 0.22 if has_train else 0.3
    offsets = np.linspace(-(n_grp - 1) * width / 2,
                           (n_grp - 1) * width / 2, n_grp)
    # offsets[0] = leftmost, offsets[-1] = rightmost — always explicit
    # 3-bar layout: [Train, CV, Test] at offsets[0,1,2]
    # 2-bar layout: [CV,    Test]     at offsets[0,1]
    cv_off   = offsets[1] if has_train else offsets[0]
    test_off = offsets[2] if has_train else offsets[1]

    cv_means  = np.array([metrics[m].get("cv_accuracy_mean", 0) * 100
                          for m in model_names])
    cv_stds   = np.array([metrics[m].get("cv_accuracy_std",  0) * 100
                          for m in model_names])
    test_accs = np.array([metrics[m].get("test_accuracy", 0) * 100
                          for m in model_names])

    bar_sets = []

    if has_train:
        train_accs = np.array([metrics[m].get("train_accuracy", 0) * 100
                               for m in model_names])
        b_tr = ax.bar(x + offsets[0], train_accs, width,
                      label="Train Accuracy (non-aug)",
                      color=STYLE["a3"], edgecolor="white", alpha=0.9)
        bar_sets.append((b_tr, train_accs))

    b_cv = ax.bar(x + cv_off, cv_means, width,
                  label="CV Accuracy (mean ± std)",
                  color=STYLE["a1"], edgecolor="white", alpha=0.9)
    ax.errorbar(x + cv_off, cv_means, yerr=cv_stds, fmt="none",
                ecolor="#1e3a8a", elinewidth=1.8, capsize=5, capthick=1.8,
                zorder=5)
    bar_sets.append((b_cv, cv_means))

    b_te = ax.bar(x + test_off, test_accs, width,
                  label="Test Accuracy",
                  color=STYLE["a4"], edgecolor="white", alpha=0.9)
    bar_sets.append((b_te, test_accs))

    # Value labels on top of every bar
    for bars, vals in bar_sets:
        for bar, val in zip(bars, vals):
            ax.text(bar.get_x() + bar.get_width() / 2,
                    bar.get_height() + 0.8,
                    f"{val:.1f}%",
                    ha="center", va="bottom",
                    fontsize=8, fontweight="bold")

    ax.set_xticks(x)
    ax.set_xticklabels(model_names, fontsize=10, fontweight="bold",
                       rotation=20, ha="right")
    ax.set_ylabel("Accuracy (%)", fontsize=11, fontweight="bold")
    ax.set_ylim(0, 115)
    ax.set_title(
        "Generalisation Gap — Train / CV / Test Accuracy\n"
        "Gap between CV and Test reflects dataset size (will close with 100 recordings)",
        fontsize=12, fontweight="bold",
    )
    ax.legend(fontsize=10, framealpha=0.9, loc="lower right")

    fig.tight_layout()

    if save:
        _save_fig(fig, save_dir, "adv_03_train_cv_test_gap")
    if not no_show:
        show_all(figs=[fig])
    plt.close(fig)


