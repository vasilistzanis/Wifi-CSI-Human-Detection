def _count_frames(filepath: Path) -> int:
    """Count CSI_DATA frames without full parsing — fast line scan."""
    count = 0
    try:
        with open(filepath, "r", encoding="utf-8", errors="ignore") as f:
            for line in f:
                if line.startswith("CSI_DATA"):
                    count += 1
    except OSError:
        pass
    return count


def _estimate_windows(n_frames: int, window_size: int, step: int) -> int:
    if n_frames < window_size:
        return 0
    return max(0, (n_frames - window_size) // step + 1)


def _load_metrics(json_path: Path) -> dict:
    if not json_path.exists():
        print(f"[ERROR] metrics.json not found: {json_path}")
        print("        Run: python csi_ml_pipeline.py --classes walk_activity no_activity --save_model")
        sys.exit(1)
    with open(json_path, "r", encoding="utf-8") as f:
        return json.load(f)


def _load_cfg(models_dir: Path) -> dict:
    path = models_dir / "experiment_config.json"
    if not path.exists():
        print(f"[WARN] experiment_config.json not found in {models_dir}")
        return {}
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def _build_test_set(cfg: dict, models_dir: Path, data_dir: Path,
                    window_size: int, step: int):
    """
    Rebuild X_test / y_test from saved pipeline + experiment config.
    Used for ROC computation without re-fitting anything.
    Window size and step are read from experiment_config.json when available
    so they always match the original training evaluation exactly.
    """
    # Prefer saved experiment settings over CLI defaults so ROC windows
    # match the exact test set used during training.
    window_size = int(cfg.get("window_size", window_size)) if cfg else window_size
    step        = int(cfg.get("step",        step))        if cfg else step

    pipeline_path = models_dir / "csi_pipeline.joblib"
    le_path       = models_dir / "label_encoder.joblib"

    if not pipeline_path.exists() or not le_path.exists():
        print("[ERROR] csi_pipeline.joblib or label_encoder.joblib not found.")
        return None, None, None

    pipeline = joblib.load(pipeline_path)
    le       = joblib.load(le_path)

    test_files = cfg.get("test_files", {})
    if not test_files:
        print("[WARN] No test_files in experiment_config.json")
        return None, None, le

    X_parts, y_parts = [], []
    for cls_name, files in test_files.items():
        if cls_name not in le.classes_:
            continue
        cls_idx = list(le.classes_).index(cls_name)
        for rel in files:
            fpath = data_dir / rel
            if not fpath.exists():
                print(f"  [SKIP] {fpath} not found")
                continue
            try:
                complex_matrix, _ = load_csi_csv(fpath)
            except Exception as e:
                print(f"  [SKIP] {fpath}: {e}")
                continue
            # Transform the full recording first (matching training: temporal diff
            # reduces length by 1, then PCA+scaler are applied to the full sequence).
            # Slide windows over the already-processed data so each window is
            # (window_size, n_pca) — exactly what the classifier was trained on.
            try:
                proc_full = pipeline.transform(complex_matrix, use_pca=True).astype(np.float64)
            except Exception as e:
                print(f"  [SKIP] {fpath}: transform failed — {e}")
                continue
            n_proc = proc_full.shape[0]
            for s in range(0, n_proc - window_size + 1, step):
                proc = proc_full[s:s + window_size]
                feat = extract_features_from_window(proc, fs=pipeline.fs, cutoff_hz=pipeline.cutoff)
                if np.isfinite(feat).all():
                    X_parts.append(feat)
                    y_parts.append(cls_idx)

    if not X_parts:
        print("[WARN] No valid test windows found.")
        return None, None, le

    return np.array(X_parts), np.array(y_parts), le


