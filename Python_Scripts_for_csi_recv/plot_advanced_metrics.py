def plot_cv_fold_scores(
    metrics: dict,
    save: bool, save_dir: Path, no_show: bool,
):
    """Per-fold CV accuracy scatter + mean ± std for every model."""
    # Check if cv_scores exist
    has_folds = any(len(v.get("cv_scores", [])) > 0 for v in metrics.values())
    if not has_folds:
        print("[SKIP] CV fold scores not in metrics.json. Re-run pipeline with "
              "--save_model to generate them.")
        return

    model_names = list(metrics.keys())
    n_models    = len(model_names)

    # Chance level depends on number of classes
    n_classes   = len(next(iter(metrics.values())).get("classes", ["a", "b"]))
    chance_pct  = 100.0 / n_classes

    _apply_style()
    fig, ax = plt.subplots(figsize=(max(9, n_models * 1.4), 6))
    fig.patch.set_facecolor(STYLE["bg"])

    n_folds = 0
    for i, name in enumerate(model_names):
        scores = metrics[name].get("cv_scores", [])
        if not scores:
            continue
        scores  = np.array(scores) * 100
        mean    = scores.mean()
        std     = scores.std()
        color   = MODEL_PALETTE[i % len(MODEL_PALETTE)]
        n_folds = len(scores)

        # Individual fold dots (jittered so they don't overlap)
        jitter = np.random.default_rng(seed=i).uniform(-0.18, 0.18, n_folds)
        ax.scatter(np.full(n_folds, i) + jitter, scores,
                   color=color, s=60, zorder=3, alpha=0.85, edgecolors="white",
                   linewidth=0.5)

        # Mean bar (translucent)
        ax.bar(i, mean, width=0.55, color=color, alpha=0.25,
               edgecolor=color, linewidth=1.5, zorder=1)

        # Error bar (±1 std)
        ax.errorbar(i, mean, yerr=std, fmt="none",
                    ecolor=color, elinewidth=2.5, capsize=6, capthick=2.5,
                    zorder=4)

        # Mean label
        ax.text(i, mean + std + 0.8, f"{mean:.1f}%",
                ha="center", va="bottom", fontsize=9, fontweight="bold",
                color=color)

    ax.set_xticks(range(n_models))
    ax.set_xticklabels(model_names, fontsize=10, fontweight="bold", rotation=20,
                       ha="right")
    ax.set_ylabel("CV Accuracy (%)", fontsize=11, fontweight="bold")
    ax.set_ylim(max(0, ax.get_ylim()[0] - 5), min(105, ax.get_ylim()[1] + 8))
    # Prefer the stored splitter name from metrics.json; fall back gracefully.
    splitter_names = {v.get("cv_splitter") for v in metrics.values()
                      if v.get("cv_splitter")}
    splitter_label = splitter_names.pop() if len(splitter_names) == 1 else "GroupKFold"
    ax.set_title(
        f"Cross-Validation Fold Scores  ({n_folds}-Fold {splitter_label})\n"
        "Dots = individual folds  |  Bar = mean  |  Error bar = ±1 std",
        fontsize=12, fontweight="bold",
    )
    ax.axhline(chance_pct, color=STYLE["grid"], linewidth=1, linestyle="--")
    ax.text(n_models - 0.5, chance_pct + 0.5,
            f"Chance ({chance_pct:.0f}%)", fontsize=8,
            color="#888888", ha="right")

    fig.tight_layout()

    if save:
        _save_fig(fig, save_dir, "adv_02_cv_fold_scores")
    if not no_show:
        show_all(figs=[fig])
    plt.close(fig)


