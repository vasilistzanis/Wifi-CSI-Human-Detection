# -----------------------------------------------------------------------

def plot_roc_curves(
    metrics: dict, models_dir: Path, data_dir: Path, cfg: dict,
    window_size: int, step: int,
    target_models: list,
    save: bool, save_dir: Path, no_show: bool,
):
    """One-vs-rest ROC curves with AUC for each requested model."""
    from sklearn.metrics import roc_curve, auc
    from sklearn.preprocessing import label_binarize

    print("\n[INFO] Building test set for ROC computation…")
    X_test, y_test, le = _build_test_set(cfg, models_dir, data_dir,
                                         window_size, step)
    if X_test is None:
        print("[SKIP] ROC curves: could not build test set.")
        return

    classes   = list(le.classes_)
    n_classes = len(classes)

    if n_classes < 2:
        print("[SKIP] ROC curves: need at least 2 classes in the test set.")
        return

    # Determine which models to plot
    if target_models and target_models != ["all"]:
        keys = [k for k in target_models if k in config.MODEL_FILES]
    else:
        keys = list(config.MODEL_FILES.keys())

    # Load requested models
    loaded = {}
    for key in keys:
        path = models_dir / config.MODEL_FILES[key]
        if not path.exists():
            print(f"  [SKIP] {path.name} not found")
            continue
        try:
            m = joblib.load(path)
            if hasattr(m, "n_features_in_") and m.n_features_in_ != X_test.shape[1]:
                print(f"  [SKIP] {key}: feature mismatch "
                      f"({m.n_features_in_} vs {X_test.shape[1]}) — re-train first")
                continue
            loaded[key] = m
        except Exception as e:
            print(f"  [SKIP] {key}: {e}")

    if not loaded:
        print("[SKIP] ROC curves: no valid models found.")
        return

    # Binarize labels for OvR
    y_bin = label_binarize(y_test, classes=list(range(n_classes)))
    if n_classes == 2:
        y_bin = np.hstack([1 - y_bin, y_bin])  # ensure shape (N, 2)

    _apply_style()

    # Layout: one subplot per class, repeated for each model
    # If many models: separate figure per model; else stack subplots
    for model_key, model in loaded.items():
        display_name = next(
            (k for k in metrics if k.lower().replace(" ", "").replace("(", "")
             .replace(")", "") == model_key.lower()), model_key.upper()
        )

        # Compute probabilities or decision scores
        if hasattr(model, "predict_proba"):
            y_score = model.predict_proba(X_test)
        elif hasattr(model, "decision_function"):
            raw = model.decision_function(X_test)
            if raw.ndim == 1:
                raw = np.column_stack([-raw, raw])
            # Softmax-like normalisation for comparability
            raw -= raw.min(axis=1, keepdims=True)
            denom = raw.sum(axis=1, keepdims=True)
            denom[denom == 0] = 1
            y_score = raw / denom
        else:
            print(f"  [SKIP] {model_key}: no predict_proba or decision_function")
            continue

        if y_score.shape[1] != n_classes:
            print(f"  [SKIP] {model_key}: score shape mismatch")
            continue

        _h = config.PLOT_ADVANCED_METRICS_SIZE[1]
        fig, axes = plt.subplots(1, n_classes,
                                  figsize=(5.5 * n_classes, _h))
        fig.patch.set_facecolor(STYLE["bg"])
        if n_classes == 1:
            axes = [axes]
        axes = np.array(axes).flatten()

        for cls_i, cls_name in enumerate(classes):
            ax = axes[cls_i]
            color = CLASS_COLORS[cls_i % len(CLASS_COLORS)]

            fpr, tpr, _ = roc_curve(y_bin[:, cls_i], y_score[:, cls_i])
            roc_auc     = auc(fpr, tpr)

            ax.plot(fpr, tpr, color=color, linewidth=2.5,
                    label=f"AUC = {roc_auc:.3f}")
            ax.fill_between(fpr, tpr, alpha=0.12, color=color)
            ax.plot([0, 1], [0, 1], color=STYLE["grid"],
                    linewidth=1.2, linestyle="--", label="Random")

            ax.set_xlim([-0.02, 1.02])
            ax.set_ylim([-0.02, 1.05])
            ax.set_xlabel("False Positive Rate", fontsize=10, fontweight="bold")
            ax.set_ylabel("True Positive Rate", fontsize=10, fontweight="bold")
            ax.set_title(
                f"ROC — {cls_name.capitalize()} (one-vs-rest)\nAUC = {roc_auc:.3f}",
                fontsize=11, fontweight="bold",
            )
            ax.legend(fontsize=9, loc="lower right", framealpha=0.9)

            # Annotate optimal threshold (top-left corner proximity)
            dist   = np.sqrt(fpr ** 2 + (1 - tpr) ** 2)
            opt_i  = np.argmin(dist)
            ax.plot(fpr[opt_i], tpr[opt_i], "o",
                    color=color, markersize=8, zorder=5,
                    label=f"Optimal threshold")
            ax.annotate(
                f"  ({fpr[opt_i]:.2f}, {tpr[opt_i]:.2f})",
                xy=(fpr[opt_i], tpr[opt_i]), fontsize=8, color=color,
            )

        # Mean AUC in suptitle
        aucs = []
        for cls_i in range(n_classes):
            fpr_i, tpr_i, _ = roc_curve(y_bin[:, cls_i], y_score[:, cls_i])
            aucs.append(auc(fpr_i, tpr_i))
        mean_auc = np.mean(aucs)

        fig.suptitle(
            f"ROC Curves — {display_name}  (One-vs-Rest)\n"
            f"Mean AUC = {mean_auc:.3f}  |  Test samples: {len(y_test)}",
            fontsize=13, fontweight="bold",
        )
        fig.tight_layout(rect=[0, 0, 1, 0.93])

        if save:
            _save_fig(fig, save_dir,
                      f"adv_04_roc_{model_key}")
        if not no_show:
            show_all(figs=[fig])
        plt.close(fig)
        print(f"  [ROC] {display_name}: mean AUC = {mean_auc:.3f}")


